
CREATE TABLE vivienda(
	viv_id VARCHAR(15) PRIMARY KEY,
	tipo_vivienda TINYINT NOT NULL,
	material_pisos TINYINT NOT NULL,
	num_cuarto TINYINT NOT NULL,
	disp_agua TINYINT NOT NULL,
	disp_elect TINYINT NOT NULL,
	tenencia TINYINT NOT NULL,
	renta INT NULL,
	total_residentes TINYINT NOT NULL,
	tam_loc INT NOT NULL,
	factor SMALLINT NOT NULL
) 

--Tabla Hogar

CREATE TABLE hogar(
	hog_id VARCHAR(20) PRIMARY KEY,
	acc_alimento TINYINT NOT NULL,
  	celular TINYINT NOT NULL,
  	conex_inte TINYINT NOT NULL,
  	num_auto TINYINT NOT NULL,
  	num_compu TINYINT NOT NULL,
  	tarjeta TINYINT NOT NULL,
  	negocio_hogar TINYINT NOT NULL,
  	est_alimentos INT NOT NULL,
  	est_trans SMALLINT NOT NULL,
  	viv_id VARCHAR(15) FOREIGN KEY REFERENCES vivienda(viv_id)
)

--Tabla persona
CREATE TABLE persona(
  	id_viv_hog_per VARCHAR(25) PRIMARY KEY,
  	sexo TINYINT NOT NULL,
  	edad TINYINT NOT NULL,
  	padre_hogar TINYINT NOT NULL,
  	nivelaprob TINYINT NULL,
  	edo_conyug TINYINT NULL,
  	segsoc TINYINT NULL,
  	horas_trabajo TINYINT NULL,
  	aten_sal TINYINT NULL,
  	prob_salud_mes DATE NULL,
  	viv_id VARCHAR(15) FOREIGN KEY REFERENCES vivienda(viv_id),
  	hog_id VARCHAR(20) FOREIGN KEY REFERENCES hogar(hog_id)
)

--Tabla  trabajo
CREATE TABLE trabajo(
	id INT IDENTITY(1,1) PRIMARY KEY,
  	subordinados TINYINT NOT NULL,
  	independiente TINYINT NULL,
  	personal TINYINT NULL,
  	horas_trabajadas TINYINT NOT NULL,
  	clase_empresa TINYINT NULL,
  	tam_empresa TINYINT NOT NULL,
  	tiene_sueldo TINYINT NULL,
  	tipo_act TINYINT NULL,
  	viv_id VARCHAR(15) FOREIGN KEY REFERENCES vivienda(viv_id),
  	hog_id VARCHAR(20) FOREIGN KEY REFERENCES hogar(hog_id),
  	id_viv_hog_per VARCHAR(25) FOREIGN KEY REFERENCES persona(id_viv_hog_per)
)

--Tabla ingreso
CREATE TABLE ingreso(
	id INT IDENTITY(1,1) PRIMARY KEY,
  	clave_ing NCHAR(4) NOT NULL,
  	ing_trim FLOAT,
  	viv_id VARCHAR(15) FOREIGN KEY REFERENCES vivienda(viv_id),
  	hog_id VARCHAR(20) FOREIGN KEY REFERENCES hogar(hog_id),
  	id_viv_hog_per VARCHAR(25) FOREIGN KEY REFERENCES persona(id_viv_hog_per)
)

--Tabla gastospersona

CREATE TABLE gastospersona(
	id INT IDENTITY(1,1) PRIMARY KEY,
  	clave NCHAR(4) NOT NULL,
  	mes_diagasto DATE NULL,
  	frec_remuneracion TINYINT NULL,
  	forma_pago TINYINT NOT NULL,
  	cantidad FLOAT NULL,
  	costo FLOAT NULL,
  	gasto_tri FLOAT NULL,
  	gasto_nm_tri FLOAT NULL,
    viv_id VARCHAR(15) FOREIGN KEY REFERENCES vivienda(viv_id),
  	hog_id VARCHAR(20) FOREIGN KEY REFERENCES hogar(hog_id),
  	id_viv_hog_per VARCHAR(25) FOREIGN KEY REFERENCES persona(id_viv_hog_per)  	
)


